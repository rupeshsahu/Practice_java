package java_practice;
class switchcase{

    public static void main(String args[]){

        int a=12,b=22;

        switch(a+b){

            case 33:
            System.out.println("total is 33");
        break;
            case 44:
            System.out.println("total is 44");
            break;


            default:
            System.out.println("default statement");

        }
    }
}